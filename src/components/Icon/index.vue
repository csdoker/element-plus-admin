<template>
  <svg :class="svgClass"
    aria-hidden="true">
    <use :xlink:href="iconName" />
  </svg>
</template>

<script>
export default {
  name: 'DzIcon',

  props: {
    iconClass: {
      type: String,
      required: true
    },

    className: {
      type: String,
      default: ''
    }
  },

  computed: {
    svgClass () {
      return this.className ? `svg-icon ${this.className}` : 'svg-icon'
    },

    iconName () {
      return `#icon-${this.iconClass}`
    }
  }
}
</script>

<style lang="scss">
.svg-icon {
  width: 1em;
  height: 1em;
  vertical-align: -0.15em;
  fill: currentColor;
  overflow: hidden;
}
</style>
